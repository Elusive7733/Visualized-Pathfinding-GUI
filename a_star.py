def heuristic_func(cordinate_1, cordinate_2):
    #manhattan distance (moving in straight lines)
    x1, y1 = cordinate_1
    x2, y2 = cordinate_2
    #adding the difference between the x and y cordinates of the grid
    return abs(x1 - x2) + abs(y1 - y2)

